#[test]
fn blake3_digest() {
    assert_eq!(
        cck::hash::blake3_digest(&[], &[]),
        [
            175, 19, 73, 185, 245, 249, 161, 166, 160, 64, 77, 234, 54, 220, 201, 73, 155, 203, 37,
            201, 173, 193, 18, 183, 204, 154, 147, 202, 228, 31, 50, 98
        ]
    );
}

